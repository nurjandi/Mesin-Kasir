    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Kasir_Form kasir = new Kasir_Form();
        Admin_Form admin = new Admin_Form();
        String sql = "select * from pegawai where ID = '"+jTextField1.getText()+"' "
                + "and password = '"+jPasswordField1.getText()+"' and "
                + "jenis_pegawai = '"+jComboBox1.getSelectedItem()+"';";
        try {
            ResultSet rss = stt.executeQuery(sql);
            if(rss.next()){
                String jenis = rss.getString("jenis_pegawai");
                if(jenis.equals("admin")){
                    JOptionPane.showMessageDialog(null,"login sebagai admin berhasil");
                    admin.setVisible(true);
                    this.dispose();
                }
                else if(jenis.equals("kasir")){
                    JOptionPane.showMessageDialog(null,"login sebagai kasir berhasil");
                    kasir.setVisible(true);
                    this.dispose();
                }
            }
            else{
                    JOptionPane.showMessageDialog(null,"username dan password salah");
                }
        } catch (SQLException ex) {
            Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

