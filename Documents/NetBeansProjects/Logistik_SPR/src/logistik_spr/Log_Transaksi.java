    public void initTable(){
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel(); 
        String sql = "select * from log_transaksi";
        try {
            ResultSet rs = stt.executeQuery(sql);
            while(rs.next()) {
                data[0] = rs.getString("kode_transaksi");
                data[1] = rs.getString("kode_barang");
                data[2] = rs.getString("nama_barang");
                data[3] = rs.getString("kuantiti");
                data[4] = rs.getFloat("harga");
                data[5] = rs.getFloat("total");
                data[6] = rs.getString("waktu");
                dtm.addRow(data);
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(Log_Transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
            jTable1.setModel(dtm);
    }
    public void Refresh(){
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        initTable();
    }
